namespace IsometricWorld.Core.World
{
    /// <summary>
    /// Базові біоми ігрового світу.
    /// Значення підібрані так, щоб відображати природну вертикальну
    /// прогресію від рівня моря до гір (зручно для порівнянь з Height).
    /// </summary>
    public enum TileType : byte
    {
        /// <summary>Відкритий океан / глибока вода.</summary>
        Ocean    = 0,

        /// <summary>Піщаний берег, мілководдя.</summary>
        Beach    = 1,

        /// <summary>Рівнина — основна ігрова зона.</summary>
        Plains   = 2,

        /// <summary>Ліс, густа рослинність.</summary>
        Forest   = 3,

        /// <summary>Гірська місцевість, скелі.</summary>
        Mountain = 4,

        /// <summary>Річка / русло проточної води.</summary>
        River    = 5,
    }
}
